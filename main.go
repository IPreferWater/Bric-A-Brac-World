const (
	screenWidth  = 768
	screenHeight = 480
)

const (
	tileSize = 24
)

var (
	tilesImage         *ebiten.Image
	mainCharacterImage *ebiten.Image
)

func init() {
	tilesEbitenImage, _, err := ebitenutil.NewImageFromFile("./res/tiles/room.png")
	if err != nil {
		log.Fatal(err)
	}

	mainCharacterEbitenImage, _, err := ebitenutil.NewImageFromFile("./res/main_character.png")
	if err != nil {
		log.Fatal(err)
	}

	tilesImage = tilesEbitenImage
	mainCharacterImage = mainCharacterEbitenImage
}

type MainCharacter struct {
	X float32
	Y float32
}

type Game struct {
	layers        [][]int
	MainCharacter MainCharacter
}

func (g *Game) Update() error {
	return nil
}

func (g *Game) Draw(screen *ebiten.Image) {

	halfHeightScreen := float32(screenHeight / 2)
	yStart, yStop := (g.MainCharacter.Y-halfHeightScreen)/tileSize, (g.MainCharacter.Y+halfHeightScreen)/tileSize
	halfWidthScreen := float32(screenWidth / 2)
	xStart, xStop := (g.MainCharacter.X-halfWidthScreen)/tileSize, (g.MainCharacter.Y+halfWidthScreen)/tileSize

	
	//Y tiles
	for i := yStart; i < yStop; i++ {
		//X tiles
		for j := xStart; j < xStop; j++ {
			op := &ebiten.DrawImageOptions{}
			op.GeoM.Translate(float64(j-xStart)*tileSize, float64(i-yStart)*tileSize)

			tile := g.layers[int(i)][int(j)]
			sx, sy := getTileCoordinate(tile)
			screen.DrawImage(tilesImage.SubImage(image.Rect(sx, sy, sx+tileSize, sy+tileSize)).(*ebiten.Image), op)
		}
	}

	characterDraw, charOpts := drawCharacter(g)
	screen.DrawImage(characterDraw, charOpts)

	ebitenutil.DebugPrint(screen, fmt.Sprintf("TPS: %0.2f", ebiten.CurrentTPS()))
}

func drawCharacter(g *Game) (*ebiten.Image, *ebiten.DrawImageOptions) {
	//put the char in the center of div

	opChar := &ebiten.DrawImageOptions{}
	opChar.GeoM.Translate(screenWidth/2, screenHeight/2)
	//opChar.GeoM.Scale(4,4)

	return mainCharacterImage.SubImage(image.Rect(0, 0, 32, 48)).(*ebiten.Image), opChar

}

